class LibFinalConsonant:
    def __init__(self):
        self.final_consonants = final_consonants['finalConsonants']
        self.final_symbol = final_consonants['finalSymbol']
    
    def get_all_groups(self):
        return self.final_consonants
    
    def get_by_letter(self, letter):
        for group in self.final_consonants:
            for member in group['members']:
                if (member.get('consonant') == letter or 
                    member.get('final') == letter or 
                    member.get('final2') == letter):
                    return member
        return None
    
    def get_group_by_letter(self, letter):
        for group in self.final_consonants:
            for member in group['members']:
                if (member.get('consonant') == letter or 
                    member.get('final') == letter or 
                    member.get('final2') == letter):
                    return group
        return None
    
    def get_by_allowed_vowel(self, compound):
        for group in self.final_consonants:
            for vowel in group['finalWith']:
                if vowel['vowel'] == compound:
                    return True
        return False
    
    def is_final_consonant(self, letter):
        return self.get_by_letter(letter) is not None
    
    def is_final_symbol(self, symbol):
        return symbol == self.final_symbol
    
    def is_final2_symbol(self, symbol):
        for group in self.final_consonants:
            for member in group['members']:
                if (member.get('final2') == symbol or 
                    member.get('final3') == symbol):
                    return True
        return False
    
    def plots(self):
        return self.final_consonants
